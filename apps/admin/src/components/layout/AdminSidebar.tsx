export function AdminSidebar(): JSX.Element {
  const pathname = usePathname();

  const renderNavItem = (item: NavItem) => {
    const isActive = pathname.startsWith(item.href);

    return (
      <li key={item.href}>
        <Link
          href={item.href}
          className={cn(
            'flex items-center gap-3 rounded-lg px-4 py-2.5 text-sm font-medium transition-colors',
            isActive
              ? 'bg-primary-50 text-primary-700'
              : 'text-secondary-600 hover:bg-secondary-50 hover:text-secondary-900'
          )}
        >
          {item.icon}
          {item.label}
        </Link>
      </li>
    );
  };

  return (
    <aside className="fixed left-0 top-0 z-40 flex h-screen w-64 flex-col border-r border-secondary-200 bg-white">
      {/* Logo */}
      <div className="flex h-16 items-center border-b border-secondary-200 px-6">
        <span className="text-xl font-bold text-primary-600">TDC Admin</span>
      </div>

      {/* Main Navigation */}
      <nav className="flex-1 overflow-y-auto p-4">
        <ul className="space-y-1">
          {navItems.map(renderNavItem)}
        </ul>
      </nav>

      {/* Bottom Navigation */}
      <nav className="border-t border-secondary-200 p-4">
        <ul className="space-y-1">
          {bottomNavItems.map(renderNavItem)}
        </ul>
      </nav>
    </aside>
  );
}
