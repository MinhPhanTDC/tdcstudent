export function StudentSidebar(): JSX.Element {
  const pathname = usePathname();
  const { unreadCount } = useNotifications();

  return (
    <aside className="fixed left-0 top-0 z-40 h-screen w-64 border-r border-secondary-200 bg-white">
      {/* Logo and Notification Bell - Requirements: 5.1, 5.2, 5.3 */}
      <div className="flex h-16 items-center justify-between border-b border-secondary-200 px-6">
        <span className="text-xl font-bold text-primary-600">TDC Student</span>
        {/* Notification Bell Icon - Requirements: 5.1, 5.3 */}
        <Link
          href="/notifications"
          className="relative rounded-lg p-2 text-secondary-600 transition-colors hover:bg-secondary-50 hover:text-secondary-900"
          aria-label="Thông báo"
        >
          <BellIcon />
          {/* Unread Badge Indicator - Requirements: 2.2, 5.2, 5.4 */}
          {unreadCount > 0 && (
            <span className="absolute right-1 top-1 flex h-2 w-2">
              <span className="absolute inline-flex h-full w-full animate-ping rounded-full bg-red-400 opacity-75"></span>
              <span className="relative inline-flex h-2 w-2 rounded-full bg-red-500"></span>
            </span>
          )}
        </Link>
      </div>

      {/* Navigation */}
      <nav className="p-4">
        <ul className="space-y-1">
          {navItems.map((item) => {
            const isActive = pathname.startsWith(item.href);
            const isNotificationItem = item.href === '/notifications';

            return (
              <li key={item.href}>
                <Link
                  href={item.href}
                  className={cn(
                    'flex items-center gap-3 rounded-lg px-4 py-2.5 text-sm font-medium transition-colors',
                    isActive
                      ? 'bg-primary-50 text-primary-700'
                      : 'text-secondary-600 hover:bg-secondary-50 hover:text-secondary-900'
                  )}
                >
                  <span className="relative">
                    {item.icon}
                    {/* Unread Badge on Notification Menu Item - Requirements: 2.2 */}
                    {isNotificationItem && unreadCount > 0 && (
                      <span className="absolute -right-1 -top-1 flex h-4 w-4 items-center justify-center rounded-full bg-red-500 text-[10px] font-bold text-white">
                        {unreadCount > 99 ? '99+' : unreadCount}
                      </span>
                    )}
                  </span>
                  {item.label}
                </Link>
              </li>
            );
          })}
        </ul>
      </nav>
    </aside>
  );
}
